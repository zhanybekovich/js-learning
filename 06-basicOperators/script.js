/**
 * Math operators
 */
console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 % 2);
console.log(2 ** 2);

/**
 * Concatenation
 */
console.log("Hello " + "world");

/**
 * Assignment
 */
let x = 1;
x += 1;
x -= 1;
x *= 1;
x /= 1;
x %= 1;
x **= 1;

/**
 * Increment
 */
let y = 1;
console.log(y++);

let z = 1;
console.log(++z);

/**
 * Decrement
 */
let a = 1;
console.log(a--);

let b = 1;
console.log(--b);

/**
 * Comparison
 */
console.log(1 == 1);
console.log(1 === 2);
console.log(1 != 1);
console.log(1 != 2);
console.log(1 > 1);
console.log(1 < 1);
console.log(1 >= 1);
console.log(1 <= 1);
console.log(1 >= 2);
console.log(1 <= 2);
